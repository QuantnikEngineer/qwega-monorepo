"""BRD Document Generator agent profile."""

from __future__ import annotations

import json
from typing import Any

from quantnik_evals.agent_profile import AgentProfile, register_profile
from quantnik_evals.models import DimensionScore


@register_profile
class BRDProfile(AgentProfile):
    """Evaluation profile for the BRD Document Generator agent.

    This agent conducts multi-turn conversations to gather requirements
    and generates a complete BRD document.
    Endpoint: POST /sessions/{session_id}/chat
    """

    name = "brd"
    description = "BRD Generator — multi-turn conversational BRD document creation"
    trace_name = "brd_chat"
    default_dataset = "brd-gen-eval"

    dimensions = [
        "reply_quality",
        "requirement_elicitation",
        "brd_document_quality",
        "conversation_flow",
        "completeness",
        "depth",
    ]

    output_schema = {
        "required_fields": ["session_id", "reply", "step"],
        "status_field": "step",
        "status_values": [],  # steps are dynamic
    }

    judge_prompts = {
        "reply_quality": """\
You are an expert business analyst evaluator. Assess the **quality** of
the AI agent's conversational reply during BRD creation.

## User Message
{input_text}

## Agent Reply
{output_text}

## Criteria
- Is the reply relevant to the user's message?
- Does it ask clarifying questions when needed?
- Is the language professional and clear?
- Does it guide the user toward providing complete requirements?

Score 0.0 to 1.0. Respond in JSON:
{{
  "score": <float 0.0-1.0>,
  "reasoning": "<explanation>"
}}
""",
        "requirement_elicitation": """\
You are an expert business analyst. Assess how well the agent **elicits
requirements** from the user.

## Conversation Context
{input_text}

## Agent Reply
{output_text}

## Criteria
- Does the agent ask the right questions to gather requirements?
- Does it cover functional, non-functional, and constraint aspects?
- Does it probe for missing details?
- Does it confirm understanding before proceeding?

Score 0.0 to 1.0. Respond in JSON:
{{
  "score": <float 0.0-1.0>,
  "reasoning": "<explanation>",
  "good_questions": ["<well-asked questions>"],
  "missed_areas": ["<areas not explored>"]
}}
""",
        "brd_document_quality": """\
You are an expert business analyst. Assess the **quality of the final
BRD document** generated by the agent.

## Generated BRD
{output_text}

## Criteria
- Does it follow a proper BRD structure (executive summary, scope,
  requirements, constraints, stakeholders, timeline)?
- Are requirements specific, measurable, and testable?
- Is the document professional and well-organized?
- Are assumptions and risks documented?

Score 0.0 to 1.0. Respond in JSON:
{{
  "score": <float 0.0-1.0>,
  "reasoning": "<explanation>",
  "strengths": ["<good aspects>"],
  "weaknesses": ["<areas for improvement>"]
}}
""",
        "conversation_flow": """\
You are evaluating the **conversation flow** of a BRD creation agent.

## Conversation
{input_text}

## Criteria
- Does the conversation follow a logical sequence?
- Are transitions between topics smooth?
- Does the agent maintain context across turns?
- Is the overall flow efficient (not too many redundant questions)?

Score 0.0 to 1.0. Respond in JSON:
{{
  "score": <float 0.0-1.0>,
  "reasoning": "<explanation>"
}}
""",
        "completeness": """\
You are evaluating the **completeness** of the BRD output.

## Input Requirements / Conversation
{input_text}

## Generated BRD
{output_text}

## Criteria
- Are all discussed requirements captured in the BRD?
- Are there gaps between what was discussed and what's documented?
- Are edge cases and dependencies covered?

Score 0.0 to 1.0. Respond in JSON:
{{
  "score": <float 0.0-1.0>,
  "reasoning": "<explanation>",
  "covered": ["<captured requirements>"],
  "missing": ["<missed requirements>"]
}}
""",
    }

    def extract_primary_input_text(self, input_data: dict[str, Any]) -> str:
        for key in ("message", "chat_history", "conversation"):
            if key in input_data and isinstance(input_data[key], str):
                return input_data[key]
        gen = input_data.get("generation_input", {})
        if isinstance(gen, dict):
            for key in ("message", "content", "text"):
                if key in gen:
                    return str(gen[key])
        return json.dumps(input_data, indent=2, default=str)
